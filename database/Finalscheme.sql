  SELECT 
    s.StaffID, s.UserID,
    u.FirstName || ' ' || u.LastName AS AdminName,
    u.Email, u.Phone, s.Department, s.JoinDate, s.Salary
FROM STAFF s
JOIN USERS u ON s.UserID = u.UserID
WHERE s.StaffType = 'ADMIN'
;
